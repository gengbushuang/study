public class Log {
    private String flow_id;

    private long req;

    private long bid;

    private long exp;

    private long clk;

    private long invest;

    private long active;

    private int type;

    private int cre_day;

    private int cre_hour;

    public String getFlow_id() {
        return flow_id;
    }

    public void setFlow_id(String flow_id) {
        this.flow_id = flow_id;
    }

    public long getReq() {
        return req;
    }

    public void setReq(long req) {
        this.req = req;
    }

    public long getBid() {
        return bid;
    }

    public void setBid(long bid) {
        this.bid = bid;
    }

    public long getExp() {
        return exp;
    }

    public void setExp(long exp) {
        this.exp = exp;
    }

    public long getClk() {
        return clk;
    }

    public void setClk(long clk) {
        this.clk = clk;
    }

    public long getInvest() {
        return invest;
    }

    public void setInvest(long invest) {
        this.invest = invest;
    }

    public long getActive() {
        return active;
    }

    public void setActive(long active) {
        this.active = active;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getCre_day() {
        return cre_day;
    }

    public void setCre_day(int cre_day) {
        this.cre_day = cre_day;
    }

    public int getCre_hour() {
        return cre_hour;
    }

    public void setCre_hour(int cre_hour) {
        this.cre_hour = cre_hour;
    }
}
